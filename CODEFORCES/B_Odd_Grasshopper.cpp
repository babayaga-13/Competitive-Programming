ll power(ll a, ll n)
{
    ll ans = 1;
    while (n)
    {
        if (n % 2)
            ans = ((__int128_t)ans * a) % MOD, n--;
        a = ((__int128_t)a * a) % MOD, n /= 2;
    }
    return ans;
}
int phi(int n)
{
    int result = n;
    for (int i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
        {
            while (n % i == 0)
                n /= i;
            result -= result / i;
        }
    }
    if (n > 1)
        result -= result / n;
    return result;
}

void solve()
{
    ll m, n;
    cin >> m >> n;
    ll y = n % 4;
    ll x = n - n % 4;
    for (ll i = 1; i <= y; i++)
    {
        if (m % 2)
        {
            m += (i + x);
        }
        else
        {
            m -= (i + x);
        }
    }
    cout << m << "\n";
}

