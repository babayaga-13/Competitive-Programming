const int MOD = 1e9 + 7;
ll lcm(ll a, ll b) { return a / __gcd(a, b) * b; }
ll power(ll a, ll n)
{
    ll ans = 1;
    while (n)
    {
        if (n % 2)
            ans = ((__int128_t)ans * a) % MOD, n--;
        a = ((__int128_t)a * a) % MOD, n /= 2;
    }
    return ans;
}
int phi(int n)
{
    int result = n;
    for (int i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
        {
            while (n % i == 0)
                n /= i;
            result -= result / i;
        }
    }
    if (n > 1)
        result -= result / n;
    return result;
}
vector<int> a, b;
ll n;
bool check(ll x)
{
    ll y = (n + x);
    y = y - (y / 4);
    ll s2 = 0, s1 = min(y, x) * 100;
    for (int i = 0; i < min(n, y); i++)
        s2 += (b[i]);
    y -= x;

    for (int i = 0; i < y; i++)
        s1 += a[i];
    if (s1 >= s2)
        return true;
    return false;
}
void solve()
{
    a.clear();
    b.clear();
    cin >> n;
    for (int i = 0; i < n; i++)
    {
        int x;
        cin >> x;
        a.push_back(x);
    }
    for (int i = 0; i < n; i++)
    {
        int x;
        cin >> x;
        b.push_back(x);
    }
    sort(rall(a));
    sort(rall(b));
    ll l = 0, h = INT_MAX, ans = 0;
    while (l <= h)
    {
        ll m = (l + h) / 2;
        if (check(m))
        {
            ans = m;
            h = m - 1;
        }
        else
            l = m + 1;
    }
    cout << ans << endl;
}

